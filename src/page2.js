/* eslint-disable space-before-function-paren */
function addNumbers(a, b) {
  // eslint-disable-next-line semi
  return a + b;
}
// eslint-disable-next-line semi
console.log(`2 + 3 => ${addNumbers(2, 3)}`);
function multiplyNumbers(a, b) {
  // eslint-disable-next-line semi
  return a * b;
}
// eslint-disable-next-line semi
console.log(`2 * 3 => ${multiplyNumbers(2, 3)}`);
